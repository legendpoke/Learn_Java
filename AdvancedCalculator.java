public class AdvancedCalculator extends NormalCalculator{
    // AdvancedCalculator inherits from NormalCalculator.
    // AdvancedCalculator is a subclass.
    // AdvancedCalculator "is a" NormalCalculator.
    // Use the extends keyword to inherit from a class.
    
    public int multi(int n1,int n2){
        return n1 * n2;
    }

    public int divide(int n1,int n2){
        return n1/n2;
    }
}
