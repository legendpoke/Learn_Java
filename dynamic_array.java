public class dynamic_array{
    public static void main(String[]args){
        // Dynamic array.
        int num[] = new int [4];
        System.out.println(num[0]);
        System.out.println(num[1]);
        System.out.println(num[2]);
        System.out.println(num[3]);
        // Dynamic array after updating a value.
        num[1] = 8;
        num[2] = 9;
        num[3] = 10;
        System.out.println(num[0]);
        System.out.println(num[1]);
        System.out.println(num[2]);
        System.out.println(num[3]);

    }
}
